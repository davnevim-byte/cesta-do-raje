export const getTileById = (id) =>
  [...OUTER_TILES, ...INNER_TILES].find((t) => t.id === id);

export const getRandomInstruction = (tile) => {
  if (!tile.instructions || tile.instructions.length === 0) return null;
  return tile.instructions[Math.floor(Math.random() * tile.instructions.length)];
};

export const getTileAtPosition = (circle, position) => {
  const tiles = circle === "outer" ? OUTER_TILES : INNER_TILES;
  return tiles[position % tiles.length] || null;
};

